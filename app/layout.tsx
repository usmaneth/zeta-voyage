import type { Metadata } from "next";
import { Geist, Geist_Mono, Playfair_Display } from "next/font/google";
import "./globals.css";
import { PrivyAuthProvider, DatabaseProvider, GoogleAuthProvider, BackupAuthProvider } from "./providers";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

const playfair = Playfair_Display({
  variable: "--font-playfair",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Voyage | Luxury AI Travel Concierge",
  description: "Your personal AI-powered luxury travel concierge. Discover exclusive destinations, curated itineraries, and bespoke travel experiences.",
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" suppressHydrationWarning>
      <head>
        <script
          dangerouslySetInnerHTML={{
            __html: `
              (function() {
                try {
                  var theme = localStorage.getItem('app_theme');
                  if (theme === 'dark') {
                    document.documentElement.classList.add('dark');
                  }
                } catch (e) {}
              })();
            `,
          }}
        />
      </head>
      <body
        className={`${geistSans.variable} ${geistMono.variable} ${playfair.variable} antialiased`}
      >
        <PrivyAuthProvider>
          <DatabaseProvider>
            <BackupAuthProvider>
              <GoogleAuthProvider>{children}</GoogleAuthProvider>
            </BackupAuthProvider>
          </DatabaseProvider>
        </PrivyAuthProvider>
      </body>
    </html>
  );
}
